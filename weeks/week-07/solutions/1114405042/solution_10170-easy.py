# 這是更簡單、更容易記憶的寫法 (Easy version)
#
# 解題思路 (While 迴圈法)：
# 1. 想像一個旅行團接著一個旅行團住。
#    - 第一個旅行團 S 人，會住 S 天。所以，總共經過了 S 天。
#    - 我們直接從總天數 D 扣除 S 天。
#    - 然後，人數變成 S + 1，會住 S + 1 天。
# 2. 如果 D 的天數比當前旅行團的人數 (S) 還大，代表 D 落在後面的旅行團中。
#    因此我們一直將 D 扣掉 S，並讓 S 增加 1，直到 D 小於等於 S 為止。
# 3. 此時的 S 就是正在住的旅行團的人數！
#
# 特點：
# 這方法簡直是直譯了題目敘述，非常簡單好寫，完全不需要去想什麼等差級數公式。
# 不過這個方法的最壞時間複雜度是 O(sqrt(D))，在 D = 10^15 時大約是 4千萬 次運算。
# 雖然對 C++ 或 Java 而言幾乎是瞬間執行完畢，但在 Python 裡面可能會花上 1 到 2 秒鐘。
# 在許多基礎演算法平台（比如 ZeroJudge 寬鬆的限制）中仍然能通過！

def solve_easy(s: int, d: int) -> int:
    """
    計算第 D 天入住的旅行團人數 (簡易版：使用 While 迴圈遞減)。
    
    參數:
    s (int): 起始旅行團的人數
    d (int): 指定查詢的天數
    
    回傳:
    int: 第 D 天的旅行團人數
    """
    # 當剩下的天數 D 還大於當前旅行團會住的天數 S
    while d > s:
        # 直接把這個旅行團住完的天數扣掉
        d -= s
        # 然後換下一個旅行團進來，人數多 1
        s += 1
        
    # 當 D 小於等於 S 時，代表在第 D 天正是這個 S 人的旅行團在住！
    return s
