# 題目 10170 (UVA 10170 - The Hotel with Infinite Rooms)
#
# 題意：
# 一家旅館有無限多間房間，每天只接待一個旅行團。
# 第一個旅行團人數為 S，且會住 S 天。
# 第二個旅行團人數為 S+1，會住 S+1 天。
# 依此類推，第 n 個旅行團人數為 S+n-1，會住 S+n-1 天。
# 給定起始旅行團人數 S 和指定的天數 D (D 可高達 10^15)。
# 請求出在第 D 天入住的旅行團有多少人。
#
# 解法 (二分搜尋法 Binary Search)：
# 因為 D 最大可以到 10^15，如果我們用一天一天加或是一個旅行團一個旅行團加的方式，
# 最糟的情況可能會需要執行幾千萬次，會稍嫌耗時。
# 假設我們計算從人數為 S 到人數為 X 的總天數，
# 根據梯形公式：(上底 + 下底) * 高 / 2 = (S + X) * (X - S + 1) / 2
# 我們只需要找出「最小的 X」，使得這個總天數大於等於 D 即可。
# 因為天數隨著 X 遞增，這是個單調遞增函數，可以使用 O(log N) 的二分搜尋法。
#
# 時間複雜度：O(log N)，N 最大為 10^8，大約只需要執行 30 次以內。
# 空間複雜度：O(1)

def solve(s: int, d: int) -> int:
    """
    計算第 D 天入住的旅行團人數 (標準二分搜尋法)。
    
    :param s: 起始旅行團的人數
    :param d: 指定查詢的天數
    :return: 第 D 天的旅行團人數
    """
    left = s
    # 根據數學推導，(X^2) / 2 約為 10^15 時，X 大約是 4.5 * 10^7。
    # 為了安全起見，我們將右界設定為 1 億 (10^8)。
    right = 100000000 
    ans = right
    
    while left <= right:
        mid = (left + right) // 2
        
        # 計算從人數 S 加到人數 mid 的總天數 (等差數列求和公式)
        # 總和 = (首項 + 末項) * 項數 / 2
        total_days = (s + mid) * (mid - s + 1) // 2
        
        # 如果加總天數大於等於 D，代表答案可能是 mid 或者更小
        if total_days >= d:
            ans = mid
            right = mid - 1  # 嘗試尋找更小且滿足條件的 X
        else:
            left = mid + 1   # 總天數還不夠，代表 X 要更大
            
    return ans
