import sys

# 這是針對 UVA 10041 (Vito's Family) 題目的簡易且好記的解法！
#
# 【解題核心概念】：
# 要讓 Vito 到所有親戚家的「總距離最小」，那他應該把家蓋在所有親戚家的「中位數」位置。
# 為什麼是中位數呢？因為在數學上一維座標上到所有點距離和最小的位置，就是中位數。
# 因此，這題最簡單的寫法就是：1. 排序 -> 2. 找中位數 -> 3. 算距離和。

def main():
    # 1. 一次讀進所有文字，以空白或換行符號切割，並全部轉換成整數列表
    # 這是競賽程式最簡單好記的讀取方式，不用管到底換了幾次行
    data = list(map(int, sys.stdin.read().split()))
    
    # 檢查是否有輸入資料，避免出錯
    if not data:
        return
        
    # data[0] 是測資的總組數，這裡我們實際上不需要用到它來做迴圈
    # 我們只需要一個指標 idx，從第 1 個位置（也就是第一組的親戚數量 r）開始讀取即可
    idx = 1
    
    # 只要指標 idx 還在資料長度內，就繼續處理下一組測資
    while idx < len(data):
        # 目前指到的數字 r，代表這組測資有幾個親戚
        r = data[idx]
        
        # 往後取 r 個數字，這就是所有親戚的門牌號碼
        # 我們直接把取出的列表進行排序 (sorted)，因為要找中位數一定要先排序！
        relatives = sorted(data[idx + 1 : idx + 1 + r])
        
        # 找中位數 (Median)：
        # 因為已經排序好了，直接取正中間的那個數字即可。
        # 如果是奇數個，r // 2 剛好就是中間；如果是偶數個，取中間兩個的右邊或左邊都可以（結果相同）。
        mid = relatives[r // 2]
        
        # 計算總距離並印出：
        # 用生成式 (Generator Expression) 把每個門牌號碼 x 跟中位數 mid 的距離 (絕對值 abs) 加總 (sum)
        print(sum(abs(x - mid) for x in relatives))
        
        # 處理完這組測資，要把指標往後推到下一組的開頭
        # 下一組的開頭位置 = 目前位置 + 1 個數量值 r + r 個門牌號碼
        idx += 1 + r

if __name__ == '__main__':
    main()
