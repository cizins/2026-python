import sys

# 這是針對 UVA 10050 (Hartals) 題目的簡易且好記的解法！
#
# 【解題核心概念】：
# 1. 把問題想像成一張日曆，從第 1 天到第 N 天。
# 2. 如果這一天是「星期五 (day % 7 == 6)」或是「星期六 (day % 7 == 0)」，那就是放假，罷會不影響工作天。
# 3. 否則，這一天是工作天。我們只要檢查這一天是不是「任何一個政黨的罷會日 (day % h == 0)」。
# 4. 如果是工作天，又剛好有人罷會，這天就報銷了，計數器 +1。

def main():
    # 1. 一次讀進所有文字，以空白或換行符號切割，並全部轉換成整數列表
    # 這是競賽程式最簡單好記的讀取方式，不用管到底換了幾次行
    data = list(map(int, sys.stdin.read().split()))
    
    # 檢查是否有輸入資料，避免出錯
    if not data:
        return
        
    # data[0] 是測資的總組數，這裡我們實際上不需要用到它來做迴圈
    # 我們只需要一個指標 idx，從第 1 個位置（也就是第一組的天數 N）開始讀取即可
    idx = 1
    
    # 只要指標 idx 還在資料長度內，就繼續處理下一組測資
    while idx < len(data):
        # 讀取模擬的天數 n
        n = data[idx]
        
        # 讀取政黨的數量 p
        p = data[idx + 1]
        
        # 往後取 p 個數字，這就是所有政黨的罷會參數 (h_i)
        hartals = data[idx + 2 : idx + 2 + p]
        
        # 用一個計數器紀錄損失的工作天數
        lost_days = 0
        
        # 逐日檢查每一天：從第 1 天檢查到第 N 天
        for day in range(1, n + 1):
            # 如果這一天不是星期五 (6) 也不是星期六 (0)，就是工作天
            if day % 7 != 6 and day % 7 != 0:
                # 只要這一天能被「任何一個」政黨的罷會參數整除 (也就是罷會日)
                # any() 函式非常好用，只要括號內的條件有一個成立就會回傳 True
                if any(day % h == 0 for h in hartals):
                    lost_days += 1
                    
        # 印出損失的總天數
        print(lost_days)
        
        # 處理完這組測資，要把指標往後推到下一組的開頭
        # 下一組的開頭位置 = 目前位置 + 1 個天數 N + 1 個政黨數 P + P 個罷會參數
        idx += 2 + p

if __name__ == '__main__':
    main()
