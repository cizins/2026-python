import sys

# 這是針對 UVA 10056 (What is the Probability?) 的超好記解法！
#
# 【解題核心概念】：
# 這是一題「高中數學」的無限等比級數總和！
#
# - 假設單次失敗的機率是 q = 1 - p。
# - 我們要算「第 i 個玩家」贏的機率，也就是「前 i-1 個人都失敗」且「輪到他時成功」的機率。
# - 這只是一輪！如果第一輪他沒贏，那第二輪他贏的機率是多少？
# - 第二輪代表第一輪 N 個人全軍覆沒，機率多了 q^n。
# 
# 整個機率 = 第一輪贏 + 第二輪贏 + 第三輪贏 + ...
# 這是一個無限等比級數：
# 首項 a (第一輪他贏的機率) = (q ** (i - 1)) * p
# 公比 r (每過一輪全軍覆沒的機率) = q ** n
# 
# 所以，套用無限等比級數公式：Sum = a / (1 - r) 就可以秒殺這題！

def main():
    # 讀取所有的資料，以空白、換行分割為字串列表
    data = sys.stdin.read().split()
    if not data:
        return
        
    idx = 1 # 指標，指向目前要處理的資料，跳過 data[0] 的測資數量
    
    while idx < len(data):
        # 讀取玩家數量 N (需要轉整數)
        n = int(data[idx])
        # 讀取成功機率 p (需要轉浮點數)
        p = float(data[idx + 1])
        # 讀取目標玩家 i (需要轉整數)
        i = int(data[idx + 2])
        idx += 3
        
        # 這是題目的唯一陷阱：如果成功機率是 0，任何人贏的機率都是 0
        if p == 0:
            print("0.0000")
            continue
            
        # q 為單次失敗的機率
        q = 1.0 - p
        
        # 首項 a：在第一輪，前 i-1 個人失敗，第 i 個人成功的機率
        a = (q ** (i - 1)) * p
        
        # 公比 r：一整輪 N 個人全部失敗的機率
        r = q ** n
        
        # 套公式求總和，並格式化為小數後四位
        probability = a / (1.0 - r)
        print(f"{probability:.4f}")

if __name__ == '__main__':
    main()
