# U7. OrderedDict 的取捨：保序但更吃記憶體（1.7）
#
# 這個範例展示了 collections 模組中 OrderedDict (有順序的字典) 的特性與代價。
# 
# 歷史背景：
# 在 Python 3.6 之前，內建的 dict (字典) 是「無序」的，你插入的順序跟迭代出來的順序可能不一樣。
# 如果你需要記住資料插入的先後順序，就必須使用 OrderedDict。
# (註：從 Python 3.7 開始，內建的 dict 也保證了維持插入順序，但 OrderedDict 仍有其不可取代的特殊用途。)

from collections import OrderedDict

# 建立一個 OrderedDict
d = OrderedDict()

# 依序加入資料
d['foo'] = 1
d['bar'] = 2

# ---------------------------------------------------------
# 核心觀念：為了「保序」，OrderedDict 付出了什麼代價？
# ---------------------------------------------------------
# 為什麼為了維持插入順序，OrderedDict 會更耗記憶體？
#
# 答案：底層額外維護的「雙向鏈結串列 (Doubly Linked List)」
# 
# 為了記住每個鍵值對 (Key-Value) 是「誰先誰後」，
# OrderedDict 內部除了維護原本字典用於快速尋找的 Hash Table (雜湊表) 之外，
# 還額外建立並維護了一個隱藏的雙向鏈結串列。
# 
# 當你新增一個元素 (如 d['foo'] = 1) 時：
# 1. 它會存入 Hash Table 中。
# 2. 它會建立一個新的節點，並配置額外的指標 (Pointers) 指向「前一個加入的元素」和「後一個加入的元素」，
#    把這個新元素串接在鏈結串列的尾端。
#
# 記憶體代價 (Trade-off)：
# 因為每個元素都需要額外儲存這兩個指標 (往前指與往後指)，
# 所以 OrderedDict 消耗的記憶體大小，大約是一般 dict 的兩倍以上！
# 當你需要處理海量資料 (例如從資料庫撈出幾十萬筆記錄存成字典) 時，
# 盲目使用 OrderedDict 會造成記憶體的大幅浪費甚至不足。

# ---------------------------------------------------------
# 補充：既然現在一般的 dict 已經有順序了，何時還需要用 OrderedDict？
# ---------------------------------------------------------
# 雖然普通的 dict 現在也有順序，但 OrderedDict 仍提供了專屬的特性：
# 
# 1. 順序敏感的「相等性比較」：
#    一般 dict 只要內容（鍵值對）一樣就算相等： 
#    {'a': 1, 'b': 2} == {'b': 2, 'a': 1}  # 回傳 True
#    但 OrderedDict 會把「順序」也納入比較標準，順序不同就不等： 
#    OrderedDict([('a', 1), ('b', 2)]) == OrderedDict([('b', 2), ('a', 1)]) # 回傳 False
#
# 2. 專屬的重新排列方法 (move_to_end)：
#    OrderedDict 允許你輕易地把某個現有的 key 移到最前面或最後面。
#    這在實作 LRU Cache (最近最少使用快取) 這類需要頻繁調動順序的演算法時非常有用。
d.move_to_end('foo')              # 將 'foo' 移到最後面
d.move_to_end('bar', last=False)  # last=False 代表將 'bar' 移到最前面 (第一個)