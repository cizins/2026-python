# U10. zip 為何只能用一次（1.8）
#
# 這個範例展示了 Python 中 zip() 以及許多內建生成器 (Generator) / 迭代器 (Iterator) 的一個關鍵特性：
# 「一次性消耗 (One-time consumption)」。
#
# 許多初學者會誤以為 zip() 回傳的是一個像 List 那樣可以重複讀取、隨意存取的陣列。
# 但實際上，為了節省記憶體，zip() 回傳的是一個「迭代器」。
# 它就像是一根吸管，吸過去（讀取過）的資料就沒有了，無法再吸第二次。

prices = {'A': 2.0, 'B': 1.0, 'C': 5.0}

# 建立 zip 迭代器 z
# 這時，z 並沒有把所有配對好的 Tuple 存進記憶體裡。
# 它只是在內部記錄了：「當你跟我要資料時，我會從 prices.values() 和 prices.keys() 各拿一個給你」。
z = zip(prices.values(), prices.keys())

# ---------------------------------------------------------
# 第一次讀取 (成功)
# ---------------------------------------------------------
# 當我們呼叫 min(z) 時，min() 會在內部使用 for 迴圈，
# 不斷地向 z 索取下一個元素，直到 z 裡面所有的元素都被拿光光。
# 在這個過程中，z 吐出了所有的 (2.0, 'A'), (1.0, 'B'), (5.0, 'C')。
# min() 比較完後回傳最小值 (1.0, 'B')。
print("最小值:", min(z))  # 正常執行，回傳 (1.0, 'B')

# ---------------------------------------------------------
# 第二次讀取 (失敗 / 回傳錯誤或空值)
# ---------------------------------------------------------
# 此時的 z 已經被 min() 徹底「抽乾 (exhausted)」了。
# 它裡面已經沒有任何東西可以再吐出來。
# 如果我們試圖再次從 z 裡面找最大值 max(z) 或把它轉成 list(z)，
# max() 會發現這是一個空的迭代器，從而拋出 ValueError: max() arg is an empty sequence。

# 取消下面的註解就會噴錯！
# print("最大值:", max(z))  # ValueError

# ---------------------------------------------------------
# 正確解法：如何重複使用？
# ---------------------------------------------------------
# 解法一：每次需要時，重新產生一次 zip 迭代器
# z_min = zip(prices.values(), prices.keys())
# min_val = min(z_min)
#
# z_max = zip(prices.values(), prices.keys())
# max_val = max(z_max)

# 解法二：如果資料量不大，直接把 zip 轉成 List 存在記憶體裡
# 這樣 list_z 就可以重複使用，因為 List 是實體的資料結構，不是一次性的迭代器。
# 但請注意，如果資料量有幾百萬筆，這個解法會非常佔用記憶體！
list_z = list(zip(prices.values(), prices.keys()))
print("轉成 List 後求最小:", min(list_z))
print("轉成 List 後求最大:", max(list_z))