# U6. defaultdict 為何比手動初始化乾淨（1.6）
#
# 這個範例展示了 collections 模組中 defaultdict 的強大之處。
# 當我們需要建立一個「值為群集型態 (如 list, set) 或需要累加計數 (如 int)」的字典時，
# 使用一般字典 (dict) 常常需要處理「鍵 (key) 不存在」的例外狀況，
# 否則就會引發 KeyError。
# defaultdict 則透過提供一個「預設工廠函數 (default factory)」完美解決了這個痛點，
# 讓程式碼變得更簡潔、易讀且效能更好。

from collections import defaultdict

# 準備一些測試資料：一個包含多個 tuple 的 list
# 我們的目標是把相同 key 的 value 收集到同一個 list 中，
# 例如最後變成 {'a': [1, 2], 'b': [3]}
pairs = [('a', 1), ('a', 2), ('b', 3)]

# ---------------------------------------------------------
# 傳統手動版：繁瑣的初始化檢查
# ---------------------------------------------------------
# 使用普通的字典 d = {}
d = {}
for k, v in pairs:
    # 每次要加入資料前，都「必須」先檢查這個 key 是否已經存在於字典中。
    # 這是因為如果直接呼叫 d[k].append(v)，
    # 且 'a' 還不在字典裡，就會噴出 KeyError: 'a' 錯誤。
    if k not in d:
        # 如果不存在，就必須手動為這個 key 初始化一個空的 list
        d[k] = []
    
    # 初始化確保安全後，才能放心地將 value 加入 list 中
    d[k].append(v)

# ---------------------------------------------------------
# defaultdict 版：優雅且乾淨的寫法
# ---------------------------------------------------------
# 當建立 defaultdict 時，我們必須傳入一個「可呼叫物件 (callable)」作為參數，
# 這個參數被稱為「預設工廠函數 (default factory)」。
# 在這裡，我們傳入內建的 `list` 函數。
d2 = defaultdict(list)

for k, v in pairs:
    # 現在我們可以直接呼叫 d2[k].append(v)，完全不需要寫 if/else 檢查！
    # 
    # 底層的魔法是這樣運作的：
    # 1. 當我們試圖存取 d2['a'] 時，defaultdict 發現 'a' 還不存在。
    # 2. 它「不會」像普通字典一樣拋出 KeyError。
    # 3. 相反地，它會自動呼叫我們當初傳入的工廠函數 `list()` 來產生一個空串列 `[]`。
    # 4. 然後把這個空串列當作 'a' 的值存起來：d2['a'] = []
    # 5. 最後，程式順利執行後面的 `.append(v)`，把值加進去。
    d2[k].append(v)

# ---------------------------------------------------------
# 補充：其他常見的工廠函數
# ---------------------------------------------------------
# 1. 計數器： `defaultdict(int)`
#    當 key 不存在時，呼叫 `int()` 會回傳 0。
#    非常適合用來算字頻： d['apple'] += 1 (一開始不存在就是 0+1=1)
#
# 2. 不重複集合： `defaultdict(set)`
#    當 key 不存在時，呼叫 `set()` 會回傳空集合 set()。
#    適合用來收集不重複的元素： d['tags'].add('python')
