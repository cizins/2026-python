# U9. groupby 為何一定要先 sort（1.15）
#
# 這個範例展示了使用 itertools.groupby() 進行分組時，一個非常重要且容易犯錯的觀念：
# 「在使用 groupby 之前，資料必須先根據分組的鍵值 (Key) 進行排序 (Sort)」。
#
# 為什麼？
# 因為 Python 的 groupby 和 SQL 裡的 GROUP BY 運作原理不同。
# 為了節省記憶體並支援無限長度的資料流 (Lazy Evaluation)，
# Python 的 groupby「只會」將「連續相鄰」且鍵值相同的元素歸在同一組。
# 只要它一遇到不同的鍵值，就會立刻結束當前的群組，並開啟一個全新的群組。

from itertools import groupby
from operator import itemgetter

# 準備測試資料：一個包含多個字典的串列
rows = [
    {'date': '07/02/2012', 'x': 1},
    {'date': '07/01/2012', 'x': 2},
    {'date': '07/02/2012', 'x': 3},
]

# ---------------------------------------------------------
# 錯誤示範：沒有先排序就直接 groupby
# ---------------------------------------------------------
print("=== 未排序的 groupby ===")
# operator.itemgetter('date') 會建立一個函數，快速提取字典中的 'date' 對應的值作為判斷標準。
# 在這個情況下，資料讀取的順序是：'07/02' -> '07/01' -> '07/02'。
# 因為 groupby 只看「連續性」，它會誤以為這產生了 3 個不同的群組！
# 導致第一筆的 '07/02' 和第三筆的 '07/02' 被硬生生拆散。
for k, g in groupby(rows, key=itemgetter('date')):
    # 注意：g 是一個一次性的迭代器，這裡用 list(g) 將其消耗並印出
    print(f"Date {k}: {list(g)}")


# ---------------------------------------------------------
# 正確寫法：先排序 (Sort) 再 groupby
# ---------------------------------------------------------
print("\n=== 排序後的 groupby ===")
# 1. 關鍵步驟：先依照我們要分組的鍵值 ('date') 進行排序。
# 排序後，具有相同 'date' 的字典就會緊緊排在一起，
# 順序變成：'07/01' -> '07/02' -> '07/02'。
rows.sort(key=itemgetter('date'))

# 2. 再次執行 groupby：
# 因為相同的日期現在已經是「連續」的狀態了，
# groupby 就能正確地把它們收集到同一個群組中，產生我們預期的 2 個群組。
for k, g in groupby(rows, key=itemgetter('date')):
    print(f"Date {k}: {list(g)}")


# ---------------------------------------------------------
# 補充：排序 (sort) vs 雜湊表 (defaultdict)
# ---------------------------------------------------------
# 1. 記憶體佔用：groupby 回傳迭代器，非常省記憶體，適合一邊讀取大檔案一邊處理。
# 2. 效能考量：但 groupby 的前提是必須先排序 (時間複雜度 O(N log N))。
#    如果不考慮記憶體限制，只想求快，或者不想改變原本資料的順序，
#    使用 collections.defaultdict(list) 跑一次 O(N) 迴圈手動分類，往往會是更快的選擇。