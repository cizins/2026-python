from decimal import Decimal, localcontext
import math

# ── 3.1 四捨五入 ──────────────────────────────────────
# round(數字, 小數位數)
# 第二個參數為 1: 保留 1 位小數
print(round(1.27, 1))  # 1.3

# 保留 3 位小數
print(round(1.25361, 3))  # 1.254

# Python 的 round() 採用「銀行家捨入」（round half to even）
# 意思是碰到正好 *.5 時，會往最近的「偶數」靠攏
print(round(0.5))  # 0（銀行家捨入，取最近偶數）
print(round(2.5))  # 2

a = 1627731
# 負的小數位數代表往整數位捨入：
# -1 代表到十位、-2 代表到百位、-3 代表到千位...
print(round(a, -2))  # 1627700（對百位四捨五入）

# ── 3.2 精確浮點數 ────────────────────────────────────
# 一般 float 採二進位浮點表示，不是所有十進位小數都能精準表示
# 所以 4.2 + 2.1 會看到極小誤差
print(4.2 + 2.1)  # 6.300000000000001（有誤差）

# Decimal 使用十進位表示，適合金融、金額、需精確小數的情境
# 注意：建議用字串建立 Decimal，避免把 float 誤差帶進來
da, db = Decimal("4.2"), Decimal("2.1")
print(da + db)  # 6.3（精確）

# localcontext() 可臨時設定 Decimal 計算環境，不會污染全域設定
with localcontext() as ctx:
    # 有效位數精度（precision）= 3
    ctx.prec = 3
    print(Decimal("1.3") / Decimal("1.7"))  # 0.765

# math.fsum 會用更穩定的方式累加浮點數，減少誤差累積
# 例子中一般 sum 可能因巨大數抵消而丟失小數，fsum 可保留正確結果
print(math.fsum([1.23e18, 1, -1.23e18]))  # 1.0（正確）

# ── 3.3 數字格式化 ────────────────────────────────────
x = 1234.56789

# 固定小數位數（2 位）
print(format(x, "0.2f"))  # '1234.57'

# 欄寬 10，靠右對齊，保留 1 位小數
print(format(x, ">10.1f"))  # '    1234.6'

# 千分位分隔符號
print(format(x, ","))  # '1,234.56789'

# 同時套用千分位與小數位控制
print(format(x, "0,.2f"))  # '1,234.57'

# 科學記號（exponential notation）
print(format(x, "e"))  # '1.234568e+03'

# ── 3.4 二八十六進制 ──────────────────────────────────
n = 1234

# 轉成不同進位字串（含前綴）
# bin: 0b..., oct: 0o..., hex: 0x...
print(bin(n), oct(n), hex(n))  # 0b10011010010 0o2322 0x4d2

# format 可輸出不含前綴的進位字串
print(format(n, "b"), format(n, "x"))  # 10011010010 4d2

# int(字串, 基底) 可把不同進位字串還原成十進位整數
print(int("4d2", 16), int("2322", 8))  # 1234 1234
