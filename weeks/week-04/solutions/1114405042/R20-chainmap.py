# R20. ChainMap 合併映射（1.20）
# 這個範例展示了如何使用 collections 模組中的 ChainMap 
# 將多個字典 (或映射) 在邏輯上合併成一個單一的視圖 (View)。
# ChainMap 提供了一個快速連結多個字典的方法，讓它們看起來像一個單一的字典，這在處理變數作用域或設定檔覆蓋時非常有用。

# 從內建的 collections 模組中匯入 ChainMap 類別
from collections import ChainMap

# 1. 準備兩個字典
# a 代表第一個字典，可能是優先級較高的設定或局部變數
a = {'x': 1, 'z': 3}
# b 代表第二個字典，可能是優先級較低的預設值或全域變數
b = {'y': 2, 'z': 4}

# 2. 建立 ChainMap
# ChainMap 接受多個字典作為參數，並按照傳入的順序建立連結。
# 注意：它不是真的把字典複製並合併成一個新的大字典 (這會消耗額外的記憶體)，
# 而是建立一個動態的「視圖(View)」，底層透過一個 list 儲存這些字典的參考 (reference)。
# 因此，這讓我們可以像操作一個單一字典一樣同時存取 a 和 b，且效能極高。
c = ChainMap(a, b)

# 3. 讀取資料：順序很重要！
# 當我們使用鍵 (Key) 來查找值時，ChainMap 會「由左至右」(從第一個字典開始) 依序尋找。
# 只要在前面的字典找到符合的鍵，就會立刻回傳對應的值，並停止往後尋找。

# 尋找 'x': 會在第一個字典 a 中找到，直接回傳 1。
print(c['x'])

# 這是 ChainMap 的核心特性：鍵名衝突時的「覆蓋(遮蔽)」效果。
# 尋找 'z': 字典 a 和字典 b 都有 'z'。因為 a 排在前面，所以會回傳 a 的值 (3)。
# 字典 b 中的 'z' (4) 就被「遮蔽」了。
print(c['z'])  # 取到 a 的 z