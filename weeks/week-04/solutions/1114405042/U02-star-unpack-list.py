# U2. 星號解包為何能處理「不定長」且結果固定是 list（1.2）
# 
# 這個範例展示了 Python 中強大的「星號解包 (Star Unpacking)」技巧。
# 當我們不知道可迭代物件 (如 tuple, list) 到底有幾個元素，
# 或者只關心其中幾個特定位置的元素，剩餘的元素想全部收集起來時，
# 就可以在變數前面加上星號 `*`。

# 定義一個包含兩筆資料的 Tuple (代表只有名字和信箱，沒有電話號碼)
record = ('Dave', 'dave@example.com')

# ---------------------------------------------------------
# 星號解包的特性與運作方式
# ---------------------------------------------------------
# 這裡我們預期資料格式為：名字, 信箱, 電話1, 電話2... (不定數量的電話)
# 所以我們用 `name` 接收第一個元素，`email` 接收第二個元素。
# 最後用 `*phones` 來「收集」剩下所有的元素。
name, email, *phones = record

# 核心觀念 1：星號變數「必定」是一個 List (串列)
# 即使原本被解包的是一個 Tuple (如本例的 record)，
# 被星號 `*` 收集起來的結果，型態永遠會是 List。
# 這樣設計是為了方便後續對這些「不定長度」的元素進行新增、刪除或迭代操作。

# 核心觀念 2：即使沒有剩餘元素，也不會報錯！
# 在這個例子中，record 只有 2 個元素，剛好分給了 name 和 email。
# 由於沒有第三個以上的元素，`phones` 就會被指派為一個「空的 List」。
# 這比以前的寫法安全很多，不會因為元素數量不夠而拋出 ValueError 錯誤。
# 也就是說，執行這行後：
# name  == 'Dave'
# email == 'dave@example.com'
# phones == []  (空串列，型態仍是 list)

# ---------------------------------------------------------
# 補充範例：如果有很多電話號碼會怎樣？
# ---------------------------------------------------------
# 假設另一筆資料有兩組電話號碼：
# record2 = ('Alice', 'alice@example.com', '0912-345-678', '02-1234-5678')
# name2, email2, *phones2 = record2
# 這時：
# name2   == 'Alice'
# email2  == 'alice@example.com'
# phones2 == ['0912-345-678', '02-1234-5678'] (收集到兩個元素的 List)

# ---------------------------------------------------------
# 補充特性：星號可以放在中間或最前面
# ---------------------------------------------------------
# 星號解包不一定要放在最後面。
# 如果想捨棄/收集前面的元素，星號也可以放前面或中間（但一個解包運算式中只能有一個星號變數）。
# 例如：
# grades = [80, 95, 88, 70, 99]
# first, *middle, last = grades
# first  == 80
# middle == [95, 88, 70]
# last   == 99