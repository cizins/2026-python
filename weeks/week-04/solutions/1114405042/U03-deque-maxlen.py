# U3. deque(maxlen=N) 為何能保留最後 N 筆（1.3）
#
# 這個範例展示了 Python 中 collections 模組底下的 deque (雙向佇列)
# 搭配 `maxlen` 參數時，能自動保留「最近的 N 筆」資料的特性。
# 
# 什麼是 deque？
# deque 全名為 double-ended queue (雙向佇列)。
# 與一般的 list (串列) 相比，deque 在「頭部」與「尾部」進行新增或刪除元素時，
# 具有 O(1) 的極高時間複雜度 (效能極佳)，而 list 在頭部插入/刪除則是 O(N)。

# 從 collections 模組導入 deque 類別
from collections import deque

# ---------------------------------------------------------
# maxlen 參數的魔力：自動「擠出」舊資料
# ---------------------------------------------------------
# 當我們在建立 deque 時，傳入 `maxlen=3` 參數，
# 就等於宣告這個佇列的「最大容量」只有 3。
# 也就是說，這個佇列最多只能同時裝載 3 個元素。
q = deque(maxlen=3)

# 我們依序將 1, 2, 3, 4, 5 加入這個佇列中
for i in [1, 2, 3, 4, 5]:
    # 使用 append() 會將元素加到佇列的「右邊 (尾部)」
    q.append(i)
    
    # 讓我們模擬一下過程：
    # 迴圈 i=1: q 變成 deque([1], maxlen=3)
    # 迴圈 i=2: q 變成 deque([1, 2], maxlen=3)
    # 迴圈 i=3: q 變成 deque([1, 2, 3], maxlen=3) -> 此時容量已滿！
    # 
    # 關鍵時刻來了：
    # 迴圈 i=4: 當加入 4 時，因為容量滿了，deque 會自動把「最舊的 (最左邊的)」元素 1 擠出去。
    #           q 變成 deque([2, 3, 4], maxlen=3)
    # 迴圈 i=5: 同理，當加入 5 時，最舊的 2 被擠出去。
    #           q 變成 deque([3, 4, 5], maxlen=3)

# 最終結果：只會保留最後加入的 3 筆資料
# 也就是 [3, 4, 5]
# 
# ---------------------------------------------------------
# 為什麼這很有用？
# ---------------------------------------------------------
# 1. 紀錄「最近的歷史紀錄」：例如瀏覽器的「上一頁」紀錄、命令列的「最近輸入指令」。
# 2. 搜尋文本中的「上下文」：當你在大檔案中搜尋某個關鍵字時，
#    可以使用 maxlen=5 的 deque 來暫存最近讀過的那 5 行，
#    一旦找到關鍵字，就可以把這 5 行一起印出來作為「前文」。
# 3. 效能極高：比使用 list.append() 然後手動 list.pop(0) 或切片 list[-N:] 
#    來維持長度要快非常多。