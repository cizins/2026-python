# U8. 字典最值為何常用 zip(values, keys)（1.8）
#
# 這個範例展示了在 Python 字典中尋找「最大值或最小值」時的一個常見痛點：
# 我們通常不僅想知道「最小值是多少」，更想知道「這個最小值對應的鍵 (Key) 是誰」。
# 透過結合 zip() 與 values(), keys()，我們可以優雅地一次取得兩者。

prices = {'A': 2.0, 'B': 1.0, 'C': 5.0, 'D': 1.0}

# ---------------------------------------------------------
# 錯誤示範一：直接對字典取 min()
# ---------------------------------------------------------
# 當我們直接把字典傳給 min() 或 max() 時，Python 預設只會迭代字典的「鍵 (Key)」。
# 所以這行程式碼實際上是在比較 'A', 'B', 'C', 'D' 的字母順序。
# 結果會回傳 'A' (字母排序最小)，完全忽略了它們對應的價格 (Value)。
wrong_min_key = min(prices)            # 回傳 key 的最小值（字母序 'A'）

# ---------------------------------------------------------
# 錯誤示範二：只對字典的 values 取 min()
# ---------------------------------------------------------
# 為了比較價格，我們改用 prices.values()。
# 這樣確實能正確比較數值，並回傳真正的最小值 1.0。
# 但問題來了：我們只拿到了數值 1.0，卻無從得知這個 1.0 到底是商品 'B' 還是 'D' 的價格。
# 數值與鍵的關聯性在這裡被截斷了。
wrong_min_val = min(prices.values())   # 回傳最小 value (1.0)，但你不知道是哪個 key

# ---------------------------------------------------------
# 正確解法：使用 zip(values, keys) 將兩者綁定
# ---------------------------------------------------------
# zip() 可以將多個可迭代物件「打包」成一個個的 Tuple (元組)。
# 這裡我們刻意把 values() 放在前面，keys() 放在後面：
# zip(prices.values(), prices.keys()) 
# 這樣會產生類似這樣的資料結構：(2.0, 'A'), (1.0, 'B'), (5.0, 'C'), (1.0, 'D')
#
# Python 中 Tuple 的比較規則是「逐項比對 (Lexicographical order)」：
# 1. min() 首先會比較 Tuple 的第一個元素 (也就是價格 value)。
# 2. 如果價格不同，立刻分出勝負。
# 3. 如果價格相同 (例如 'B' 和 'D' 都是 1.0)，它就會自動比較 Tuple 的第二個元素 (也就是鍵 key)，決定誰更小 ('B' < 'D')。
#
# 因此，這行程式碼會回傳一個包含 (最小value, 對應key) 的 Tuple，一次滿足所有需求！
best_min = min(zip(prices.values(), prices.keys()))
# 回傳 (1.0, 'B')

# ---------------------------------------------------------
# 補充注意事項：
# ---------------------------------------------------------
# 1. zip() 回傳的是一個只能被消耗一次的「迭代器 (Iterator)」。
#    如果你想同時找出最小值和最大值，不能把同一個 zip() 結果存成變數重複使用，
#    必須每次都重新呼叫一次 zip()。
#
# 2. 另一種常見的替代寫法是使用 key 參數：
#    min_key = min(prices, key=lambda k: prices[k])  
#    這個寫法會回傳對應最小值的 key (即 'B')，如果你需要值，再用 prices[min_key] 取出即可。