# U4. heap 為何能高效拿 Top-N（1.4）
#
# 這個範例展示了 Python 內建的 heapq (Heap Queue) 模組的核心運作原理。
# 為什麼在尋找一堆資料中的「前 N 個最大或最小值」時，
# 我們不直接把整個 list 給 sorted() 排序，而是使用 heapq 呢？
# 因為當資料量非常大，而我們只需要拿出一小部分 (Top-N) 時，
# sorted() 會把所有資料都排好序 (時間複雜度 O(N log N))，這太浪費時間了。
# 而 heapq 透過「堆積 (Heap)」這種特殊樹狀資料結構，可以讓我們以
# O(N log k) 甚至更快的方式找出 Top-k，大大節省效能！

import heapq

# 準備一個無序的數字串列
nums = [5, 1, 9, 2]

# 為了不改變原本的 nums，我們複製一份給 h
h = nums[:]

# ---------------------------------------------------------
# 1. 將普通的 List 轉換成 Heap (堆積)
# ---------------------------------------------------------
# 使用 heapq.heapify() 可以將一個普通的 list「原地」轉換成 heap 結構。
# 這個轉換動作的時間複雜度是 O(N)，非常有效率。
heapq.heapify(h)

# 轉換後的 h 內部元素順序會改變，雖然它看起來還是一個 list (例如 [1, 2, 9, 5])，
# 但它已經滿足了「Min-Heap (最小堆積)」的性質：
# 父節點的值永遠 <= 子節點的值。
#
# 核心性質：
# 轉換完後，h[0] (也就是這個 list 的第一個元素)「絕對」是這個堆積中最小的值！
# 但請注意，h[1], h[2]... 後面的元素並不保證是由小到大排好序的，
# Heap 只保證「最頂端 (索引 0)」是最小的。

# ---------------------------------------------------------
# 2. 取出並移除最小值 (heappop)
# ---------------------------------------------------------
# 如果我們想要拿目前的最小值，可以使用 heapq.heappop()。
# 它的厲害之處在於，當它把最小值拿走後，
# 剩餘的元素會在極短的時間內 (O(log N)) 自動重新調整，
# 讓新的最小值「浮」到 h[0] 的位置，繼續維持 Min-Heap 的性質。
m = heapq.heappop(h)  

# 每次呼叫 heappop，你拿到的都保證是「目前堆積中最小」的數字。
# 在這個例子中：
# 第一次 heappop: 拿到 1，剩餘的 h 會重新調整 (這時新的 h[0] 變成 2)
# 第二次 heappop: 就會拿到 2，依此類推...

# ---------------------------------------------------------
# 補充：如果我想找最大值怎麼辦？
# ---------------------------------------------------------
# Python 的 heapq 預設是 Min-Heap (找最小值)。
# 如果你需要 Max-Heap (找最大值)，通常有兩種做法：
# 1. 把所有數字變成負數 (例如 -5, -1, -9, -2) 再塞入 heapq，
#    因為 -9 是最小的，所以最先被 pop 出來，我們拿出來後再轉回正數就變最大值 9 了。
# 2. 如果只是單純要找最大的前 N 個，可以直接使用 heapq.nlargest(N, iterable)，
#    模組底層會自動幫你處理好高效的尋找邏輯。