# U1. 解包失敗的原因：變數數量 ≠ 元素數量（1.1）
# 
# 這個範例展示了在 Python 中進行「解包 (Unpacking)」時常見的錯誤情況。
# 所謂的解包，是將一個可迭代物件（例如：Tuple 元組、List 串列、字串等）中的元素，
# 一次性指派（賦值）給多個變數。
# 
# 成功解包的黃金法則：
# 等號左邊的「變數數量」，必須完全等於等號右邊的「可迭代物件的元素數量」。

# 定義一個包含兩個整數元素的 Tuple (元組)
p = (4, 5)

# ---------------------------------------------------------
# 情境一：變數太多 (Not enough values to unpack)
# ---------------------------------------------------------
# 這裡嘗試將只有 2 個元素的 tuple p，解包給 3 個變數 (x, y, z)。
# 由於沒有第三個元素可以賦值給 z，Python 會拋出 ValueError。
# 
# 如果你取消下面這行的註解並執行，會得到：
# ValueError: not enough values to unpack (expected 3, got 2)
# 意思為：沒有足夠的值可以解包 (預期要 3 個，但只拿到 2 個)。

# x, y, z = p  # ValueError：元素只有 2 個但變數要 3 個


# ---------------------------------------------------------
# 補充情境二：變數太少 (Too many values to unpack)
# ---------------------------------------------------------
# 反過來說，如果變數數量少於元素數量，同樣會發生錯誤。
# 假設我們嘗試將 p 解包給單一個變數 (雖然這樣通常只是將整個 tuple 指派過去，
# 但如果寫成明確要解包的形式，例如加上逗號 `x, = p`)，就會報錯。

# 假設 p2 有 3 個元素
# p2 = (1, 2, 3)
# 嘗試將 3 個元素解包給 2 個變數
# a, b = p2  # ValueError: too many values to unpack (expected 2)
# 意思為：有太多的值要解包 (預期只要 2 個)。

# ---------------------------------------------------------
# 解決方案
# ---------------------------------------------------------
# 如果真的遇到數量不符的情況，但又想解包：
# 1. 確保變數數量一致： `x, y = p`
# 2. 如果只想要部分元素，可以用底線 `_` 作為不要的變數名（慣例寫法）：
#    例如 `p3 = (1, 2, 3)`
#    `x, y, _ = p3` (這樣就只拿前兩個)
# 3. 使用星號 `*` 來收集剩餘的元素（這是進階解包技巧，例如 `*rest`）。