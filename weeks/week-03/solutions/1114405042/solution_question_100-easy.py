"""
============================================================
題目 100 - Collatz 序列（3n+1 問題）簡化版實現
============================================================

問題說明
============================================================

Collatz 猜想（3n+1 問題）：
  給定任意正整數 n，執行以下操作直到 n = 1：
  1. 若 n = 1，停止
  2. 若 n 是奇數，令 n = 3*n + 1
  3. 若 n 是偶數，令 n = n / 2

Cycle-length 定義：
  從 n 開始到達 1 為止的步數（包含 n 和 1）

範例：
  n=22: 22→11→34→17→52→26→13→40→20→10→5→16→8→4→2→1，共 16 步
  
題目要求：
  給定兩個整數 i, j（可能順序不同）
  求區間 [min(i,j), max(i,j)] 內最大的 cycle-length

============================================================
核心概念簡述
============================================================

✓ 給定兩個整數 i, j，找區間內所有數的最大 cycle-length
✓ Cycle-length = 從 n 經過 Collatz 序列到達 1 的步數
✓ Collatz 規則：
  - n=1 時停止
  - n 為奇數時 n=3n+1
  - n 為偶數時 n=n/2
  
✓ 記憶化優化：避免重複計算相同的數

============================================================
"""


def cycle_length(n, memo={}):
    """
    計算整數 n 的 Collatz cycle-length。
    
    使用記憶化遞迴實現。memo 作為預設參數，會在函數定義時創建，
    所以多次呼叫此函數會共享同一個字典，實現快取效果。
    這是 Python 中函數式記憶化的經典用法。
    
    演算法流程：
    1. 檢查快取：若 n 已計算過，直接返回快取結果（O(1)）
    2. 基礎情況：n=1 時返回 1
    3. 遞迴計算：
       - 若 n 是奇數：計算 cycle_length(3*n+1) + 1
       - 若 n 是偶數：計算 cycle_length(n/2) + 1
    4. 儲存結果：將計算結果存入快取，供未來使用
    
    參數：
        n (int)：要計算的正整數
        memo (dict)：記憶化字典（預設參數，函數定義時創建）
    
    返回：
        int：n 的 cycle-length
    
    時間複雜度：
        第一次計算：O(log n)（平均）
        快取命中：O(1)
    
    例示：
        cycle_length(1) = 1       # 1 → stop
        cycle_length(2) = 2       # 2 → 1
        cycle_length(3) = 8       # 3 → 10 → 5 → 16 → 8 → 4 → 2 → 1
        cycle_length(22) = 16     # 22 → ... → 1（共16步）
    """
    # 快取查詢：若已計算過此數，直接返回
    # memo 中的鍵是整數 n，值是其 cycle-length
    # 這避免了重複計算，大幅提升效能（特別是在區間查詢中）
    if n in memo:
        return memo[n]
    
    # 基礎情況（遞迴終止條件）
    # 當 n=1 時，序列已終止，cycle-length 為 1
    if n == 1:
        return 1
    
    # 遞迴情況：根據 Collatz 規則計算下一步
    if n % 2 == 1:
        # n 是奇數：應用 n = 3*n + 1 規則
        # 例如：n=5 → 3*5+1=16（變成偶數，下次迴圈會做除法）
        # +1 表示當前這一步
        result = 1 + cycle_length(3 * n + 1, memo)
    else:
        # n 是偶數：應用 n = n / 2 規則
        # 例如：n=10 → 10/2=5
        # // 表示整數除法（在 Python 3 中推薦使用）
        # +1 表示當前這一步
        result = 1 + cycle_length(n // 2, memo)
    
    # 儲存計算結果到快取
    # 以便未來呼叫相同的 n 時直接返回，避免重複遞迴計算
    # 這是動態規劃的核心：「備忘錄法」(Memoization)
    memo[n] = result
    return result


def solve(i, j):
    """
    求區間 [min(i,j), max(i,j)] 內所有數的最大 cycle-length。
    
    此函數是問題求解的主要邏輯。
    
    步驟：
    1. 標準化區間：保證 start ≤ end
    2. 遍歷區間：計算每個數的 cycle-length
    3. 取最大值：返回最大的 cycle-length
    
    參數：
        i (int)：區間端點 1（可大可小）
        j (int)：區間端點 2（可大可小）
    
    返回值：
        tuple：(i, j, max_cycle_length)
        - i, j：原始輸入（保持輸入順序）
        - max_cycle_length：區間內最大的 cycle-length
    
    特點：
        ✓ 輸入順序無關性：solve(1,10) 和 solve(10,1) 結果相同
        ✓ 簡潔實現：使用 Python 內建函數和生成器表達式
    
    例示：
        solve(1, 10) → (1, 10, 20)
        solve(10, 1) → (10, 1, 20)
        solve(100, 200) → (100, 200, 125)
        solve(201, 210) → (201, 210, 89)
    """
    # 標準化區間順序
    # 無論輸入順序如何，都轉換為 start ≤ end
    # 這樣確保我們遍歷的是正確的區間範圍
    start, end = min(i, j), max(i, j)
    
    # 計算區間內所有數的 cycle-length，並取最大值
    # 詳細分解此行代碼：
    #
    # range(start, end + 1)
    #   生成 [start, start+1, ..., end] 的所有整數
    #   注意：range 的上界是不包含的，所以要用 end+1
    #
    # (cycle_length(n) for n in ...)
    #   生成器表達式，逐個計算每個數的 cycle-length
    #   這樣可以避免先構建整個列表，節省內存
    #
    # max(...)
    #   取所有 cycle-length 中的最大值
    #
    # 這三層構成了高效、簡潔的區間查詢邏輯
    max_len = max(cycle_length(n) for n in range(start, end + 1))
    
    # 返回三元組
    # 注意：返回的是原始 i, j，而不是標準化後的 start, end
    # 這樣保持了輸入輸出的對應關係（題目要求的格式）
    return (i, j, max_len)


def main():
    """
    主程式：從標準輸入讀取測試用例，輸出結果。
    
    與 solve() 函數的區別：
    - solve()：處理單一查詢，返回三元組
    - main()：無限迴圈讀取輸入，逐個輸出結果
    
    輸入格式：
        每行一對整數 i, j（用空格分隔）
        0 < i, j < 1,000,000（根據題目要求）
        直到遇到 EOF（End of File）或 Ctrl+D
    
    輸出格式：
        每行一個結果：i j max_cycle_length
        保持輸入的原始順序
    
    異常處理：
        - if line：自動跳過空行，避免格式錯誤
        - EOFError：遇到檔案結尾時安全終止，不拋出錯誤
    
    例示：
        輸入：
            1 10
            100 200
            201 210
        
        輸出：
            1 10 20
            100 200 125
            201 210 89
    """
    try:
        # 無限迴圈讀取標準輸入
        # 直到遇到 EOF（檔案結尾）時會拋出 EOFError
        while True:
            # 讀取一行，並移除首尾的空白（包括換行符 \n）
            # .strip() 將 "  1 10  \n" 轉換為 "1 10"
            line = input().strip()
            
            # 若為非空行，進行處理
            # 這樣可以跳過空行，避免因為空行而拋出異常
            if line:
                # 按空格分割輸入字符串，並將結果轉換為整數
                # 例如：line = "1 10"
                #      line.split() = ["1", "10"]
                #      map(int, ...) = [1, 10]
                #      i, j = 1, 10
                i, j = map(int, line.split())
                
                # 求解此輸入對
                # solve 函數返回 (i, j, max_cycle_length)
                i1, j1, max_len = solve(i, j)
                
                # 輸出結果到標準輸出
                # print 會自動添加換行符
                # 格式：「i j max_cycle_length」，用空格分隔
                print(f"{i1} {j1} {max_len}")
    
    except EOFError:
        # 遇到檔案結尾（EOF）時的正常終止
        # 此異常在以下情況發生：
        #   1. 輸入流結束（執行完所有輸入行）
        #   2. 使用者按下 Ctrl+D（在 Unix/Mac 上）或 Ctrl+Z（在 Windows 上）
        # 不需要輸出任何錯誤訊息，簡單地退出程式
        # 程式的退出碼會是 0（成功）
        pass


# ============================================================
# 程式入口（Entry Point）
# ============================================================

if __name__ == '__main__':
    """
    此代碼塊只在此檔案被直接執行時運行，
    不會在此檔案被作為模組 import 時運行。
    
    這是 Python 的標準慣例，功用：
    1. 區分「執行」和「導入」兩種情況
    2. 便於將代碼既用於獨立程式，又用於模組
    3. 避免在被 import 時執行不必要的代碼
    
    何時使用此模式：
        python3 solution_question_100-easy.py
        ↓
        __name__ == '__main__'（條件成立）
        ↓
        執行 main() 函數
    
    何時不執行此塊：
        from solution_question_100_easy import solve
        ↓
        __name__ != '__main__'（條件不成立）
        ↓
        不執行 main() 函數，但可以使用 solve() 等函數
    """
    main()
